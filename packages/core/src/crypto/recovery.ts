const WORD_COUNT = 24;
const ITERATIONS = 100000;
const KEY_LENGTH = 32;

export function generateRecoveryPhrase(): string {
  const entropy = randomBytes(32);
  const hash = createHash('sha256').update(entropy).digest();
  const combined = Buffer.concat([entropy, hash.subarray(0, 1)]);

  const words: string[] = [];
  let bits = '';

  for (const byte of combined) {
    bits += byte.toString(2).padStart(8, '0');
  }

  for (let i = 0; i < WORD_COUNT; i++) {
    const index =
      parseInt(bits.slice(i * 11, (i + 1) * 11), 2) % BIP39_WORDLIST.length;
    words.push(BIP39_WORDLIST[index]!);
  }

  return words.join(' ');
}

export function validateRecoveryPhrase(phrase: string): boolean {
  const words = phrase.trim().toLowerCase().split(/\s+/);

  if (words.length !== WORD_COUNT) {
    return false;
  }

  return words.every((word) =>
    BIP39_WORDLIST.includes(word as (typeof BIP39_WORDLIST)[number])
  );
}

export function phraseToKey(phrase: string, salt = 'engram-recovery'): Buffer {
  if (!validateRecoveryPhrase(phrase)) {
    throw new Error('Invalid recovery phrase');
  }

  return pbkdf2Sync(phrase, salt, ITERATIONS, KEY_LENGTH, 'sha512');
}
